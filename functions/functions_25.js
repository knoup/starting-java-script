// Operadores de comparação 

// Irá comparar valores e retornar um Boolean como resposta

let one = 1
let two = 2

// == igual
console.log(one == 1)
console.log(one == 2)
console.log(one == "1")

// != diferente
console.log(one != 2)
console.log(one != 1)
console.log(one != "1")

// === estritamente igual
console.log(one === "1")
console.log(one === 1)

// !== estritamente diferente
console.log(two !== "2")
console.log(two !== 2)

// > Maior
console.log( one > two)

// >= Maior ou igual
console.log(one >= 1)
console.log(two >= 1)

// < Menor
console.log(one < two)

// <= Menor igual
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)
